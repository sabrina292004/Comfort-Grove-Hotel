package JavaOOP;

public class Clients {
    class Cients {
        String firstName;
        String lastName;
        int no_Kids;
        int no_adults;
        int no_rooms;
        String roomType;
        double roomPrice;
        int phoneNumber;

        public Cients(String firstName, String lastName, int no_Kids, int no_adults, int no_rooms, String roomType, double roomPrice, int phoneNumber) {
            this.firstName = firstName;
            this.lastName = lastName;
            this.no_Kids = no_Kids;
            this.no_adults = no_adults;
            this.no_rooms = no_rooms;
            this.roomType = roomType;
            this.roomPrice = roomPrice;
            this.phoneNumber = phoneNumber;
        }
    }

}
